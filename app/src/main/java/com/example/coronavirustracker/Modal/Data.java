package com.example.coronavirustracker.Modal;

public class Data {
    private String country, confirmedcases, reporteddeaths;

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getConfirmedcases() {
        return confirmedcases;
    }

    public void setConfirmedcases(String confirmedcases) {
        this.confirmedcases = confirmedcases;
    }

    public String getReporteddeaths() {
        return reporteddeaths;
    }

    public void setReporteddeaths(String reporteddeaths) {
        this.reporteddeaths = reporteddeaths;
    }
}
